// Appwrite implementation of the database helper
const getAppwrite = () => window.appwrite || {};

window.dbInit = async () => {
    if (window.location.protocol === 'file:') {
        const msg = "⚠️ متصفحك يمنع الاتصال بـ Appwrite عند فتح الملف مباشرة (file://).\n\nيرجى تشغيل الموقع باستخدام Live Server أو رفع الملفات على استضافة، أو إضافة 'null' كـ Platform في لوحة Appwrite.";
        console.warn(msg);
        // We only alert once per session
        if (!sessionStorage.getItem('cors_warned')) {
            alert(msg);
            sessionStorage.setItem('cors_warned', 'true');
        }
    }
    return true;
};

// --- MATERIALS FUNCTIONS ---
window.dbAdd = async (item) => {
    const { databases, storage, DB_ID, COLLECTIONS, BUCKET_ID, ID } = getAppwrite();
    console.log("💾 dbAdd function (Appwrite) triggered with item:", item);
    try {
        if (!databases) throw new Error("Appwrite not initialized");
        if (item.isBlob && item.url instanceof File) {
            console.log("📤 Detected file upload. Ref:", item.id);
            const file = item.url;

            console.log(`⏳ Uploading to Appwrite storage (Bucket: ${BUCKET_ID})...`);
            // Capture the response to get the actual ID generated by the server
            const response = await storage.createFile(BUCKET_ID, ID.unique(), file);
            const actualFileId = response.$id;

            console.log(`✅ Upload complete. ID: ${actualFileId}`);

            // Use the actual ID for URL generation
            const downloadURL = storage.getFileView(BUCKET_ID, actualFileId).href;

            item.url = downloadURL;
            item.isBlob = false;
            item.storagePath = actualFileId;
        }

        const docId = String(item.id);
        console.log("📝 Attempting to save to Appwrite Database. Collection:", COLLECTIONS.MATERIALS, "ID:", docId);

        // Ensure dataToSave is a clean object for Appwrite
        const dataToSave = { ...item };
        // If url is still a File (meaning upload failed or was skipped), remove it or convert it
        if (dataToSave.url instanceof File) {
            delete dataToSave.url;
        }

        await databases.createDocument(DB_ID, COLLECTIONS.MATERIALS, docId, dataToSave);

        console.log("✅ Appwrite save successful!");
        return item;
    } catch (e) {
        console.error("❌ Appwrite Add Error:", e);
        let errorMsg = e.message || "خطأ غير معروف";
        const errorCode = e.code || "";

        if (errorCode === 401 || errorCode === 403) {
            errorMsg = "مشكلة في الصلاحيات (Permissions). تأكد من إعداد الـ Permissions في Appwrite لـ Any (create/read).";
        } else if (errorMsg.includes("Attribute not found")) {
            errorMsg = "المتصفح لم يجد بعض الحقول في Appwrite. يرجى تشغيل ملف fix-appwrite.html أولاً.";
        } else if (errorCode === 400) {
            if (errorMsg.includes("limit allowed for your plan")) {
                errorMsg = "⚠️ حجم الملف كبير جداً بالنسبة للخطة المجانية في Appwrite. يرجى رفع ملف أصغر أو استخدام رابط خارجي (مثل Google Drive/YouTube).";
            } else {
                errorMsg = "بيانات غير صالحة (Schema Mismatch). تأكد من أنواع الحقول (Integer/String). " + errorMsg;
            }
        }

        alert("خطأ (Code " + errorCode + "): " + errorMsg);
        throw e;
    }
};

